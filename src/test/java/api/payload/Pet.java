package api.payload;

public class Pet {
	
	    public String id;
	    public String name;
	    
	    
	    
	    public String getId() {
			return id;
		}
		public void setId(String string) {
			this.id = string;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		

}
